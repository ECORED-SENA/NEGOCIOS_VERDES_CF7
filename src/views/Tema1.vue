          | por el personaje del mago en la película del mismo nombre, en la cual el personaje del mago crea una ilusión de un producto real, haciendo parecer que el producto funciona completamente y es totalmente automatizado desde afuera, pero las cosas son maniobradas por personas, es decir, la operación se hace manualmente. 
        p.mb-4 Lo que se busca es ofrecer una promesa de valor y probar una suposición con el mínimo uso de la tecnología. 
        p.mb-4 
          strong Zappos:  
          | la idea del creador de este emprendimiento era vender zapatos en línea. Quería probar la siguiente hipótesis inicial: si era cierto que había público que quisiera pagar por comprar zapatos en línea. 
        p Realizó el siguiente experimento: habló con el propietario de una zapatería de su barrio, les tomó fotos a unos zapatos de su local y las publicó en su página web para venderlos. Cuando le realizaban un pedido, el fundador iba al local de zapatos donde tomaba las fotos y compraba los zapatos, los empacaba como si fueran suyos y los enviaba al cliente. Finalmente, este emprendimiento tuvo éxito, porque fue comprado por Amazon en el año 2009 por $1.200 millones de dólares. 
