        h4 Conserjería
        p.mb-4 Esta técnica tiene que ver con el aprendizaje que obtenemos de los primeros clientes del producto, ya que en un principio no se cuenta con todo el soporte para atender a un gran número de clientes, es decir, se hace todo el trabajo para los primeros. Se usa cuando se requiere entender el comportamiento del cliente y lo que se debe hacer para solucionar su problema.
        p.mb-4 
          strong Ejemplo:  
          | Una página web que ayuda a las personas a realizar su compra de alimentos saludables de forma semanal. El objetivo de este emprendimiento es ayudar a diseñar un plan de alimentación saludable por semana, en el cual se hace una lista de la compra ayudándose en los deleites personales y se comparan las ofertas de las tiendas y supermercados cercanos que ofrecen servicios similares. El emprendimiento empezó con la primera cliente trabajando para ella misma, sin tener aún una página web que computarizara el servicio, simplemente se pretendía lograr retroalimentación del cliente y sus opiniones.
      .tarjeta.color-acento-botones.p-4(titulo="Crowdfunding")
        h4 Crowdfunding
        p.mb-4 Esta prueba consiste en buscar inversión para un proyecto a través de Internet. Las personas que demuestran interés en los proyectos deciden la cantidad de dinero que desean invertir. Se requiere utilizar una plataforma de financiación colaborativa y crear un producto visual y descriptivo. Si la gente lo acepta y financia el proyecto, con eso se tiene una idea de su nivel de aceptación. El objetivo es verificar que hay un mercado para ese producto que está dispuesto a pagar por él y, además, busca validar el precio del producto.

